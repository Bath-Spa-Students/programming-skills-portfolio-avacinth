modes_of_transportation = ["car", "motorcycle", "bicycle", "scooter"]

# Print statements about these items
for item in modes_of_transportation:
    if item == "car":
        print(f"I would like to own a {item}.")
    if item == "motorcycle":
        print(f"I've always wanted a {item}.")
    if item == "bicycle":
        print(f"Riding a {item} is a great way to stay fit.")
    if item == "scooter":
        print(f"A {item} is perfect for city commuting.")